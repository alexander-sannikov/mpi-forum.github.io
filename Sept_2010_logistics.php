<p>Travel information:Best is to fly in to Stuttgart Airport (STR, which is connectedto most European hubs), e.g., flying directly from US toFrankfurt, Munich or Zurich with a connecting flight to Stuttgart(e.g., with Lufthansa, or Swiss).At the airport, you can use all "S-Bahn"s in the direction toStuttgart (with final destinations Backnang or Schorndorf)leaving every 10/20 minutes 5:08am :18, :38, :48 until 10:08pmand then every 30 min until 0:08am.
</p>

<p>Another option is to use the only one direct USA-Stuttgart flightwhich is from Atlanta to Stuttgart (Delta).From Stuttgart Airport to the hotel, the best and cheapest way isthe "S-Bahn (Blue Marker)", i.e., our light rail system that directly connectsthe airport with "Stuttgart University" station (17 minutes ride).This station is 2 minutes from the hotel (purple line) and about a 15 minuteswalk from HLRS (green line). Single ticket: 2 Zones = 2.45 Euro.The ticket machines can return the change and accept also 5 Euro bills.
</p>

<p>A third option is to fly in to Frankfurt and to use the train tocome to Stuttgart "Hauptbahnhof" (main station). The main stationis also directly connected with "Stuttgart University" (11 min ride).(This third option may cause trouble because of ongoingconstruction work at the main railway station and thereforelate or missed trains to Frankfurt!)
