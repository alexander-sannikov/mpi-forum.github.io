<p>Chicago O'Hare International Airport is served directly by train, taxi,
shared-ride van, shuttle, and bus. The Airport Transit System provides a
quick, convenient, and free service for transfers to / from the terminals.
It can also be used to reach the long-term parking facility.
CTA Train Services I checked and you would have to change trains, take a bus
and then walk, so not a good option.</p>

<p>The Chicago Transit Authority's Blue Line provides a 24-hour service between
Chicago O'Hare International Airport and downtown Chicago. Trains run every
eight minutes from 5:00 am to 8:00 pm weekdays and every ten minutes from
5:00 am to 11:00 pm on Saturday and Sunday. The CTA station is located on
the lowest level of the main parking garage and is accessible by pedestrian
tunnels from Terminals 1, 2, and 3.</p>

<p>Taxi and Shared-ride Van Services Taxi is $35-40 one-way.</p>

<p>Taxis at Chicago O'Hare International Airport are available at all terminals
from the lower level curbfront. Metered rates apply to all destinations.
Chicago O'Hare International Airport shared-ride vans are also available at
all terminals from the lower level curbfront.</p>

