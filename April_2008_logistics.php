<h3><a name="bus">Bus and Shuttle Service</a></h3>

<p>Located on the ground floor of the elevated parking garage, the bus /
shuttle center is the primary pickup point for hotel courtesy and Pace buses
as well as shuttles for car rental agencies and parking. The center is
accessible by tunnels from Terminals 1, 2, and 3.</p>

<p>Continental Airport Express shuttles leave Chicago O'Hare International
Airport every five to ten minutes. Tickets may be purchased at the kiosk
outside of the International Terminal and at the lower levels of Terminals
1, 2, and 3 at the counters across from the baggage claim. Continental
Airport Express also offers shuttles to northern suburban locations.</p>

